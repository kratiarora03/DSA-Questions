#include <iostream> 
using namespace std;
bool checkDuplicate(int arr , int n ){
    if(i= 0 , i<=n , i++){
        if(j= 0 , j<=n , j++){
            arr[i] == arr[j] ; 
            return true;
        }
    }
}

int main(){
    int arr[] = {1,2,3,1};
    int n = 4; 
     checkDuplicate(arr , n);
}
